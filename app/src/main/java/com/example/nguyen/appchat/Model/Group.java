package com.example.nguyen.appchat.Model;

public class Group {

   public String owner;
   public String name;

    public Group(){

    }

    public String getOwner() {
        return owner;
    }

    public void setOwner(String owner) {
        this.owner = owner;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
